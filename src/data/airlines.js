export const searchAirlines = (query) => {
  return new Promise((resolve) => {
    setTimeout(() => {
      const filtered = airlinesData.filter(airline =>
        airline.airline_name.toLowerCase().includes(query.toLowerCase()) ||
        airline.airline_name_english.toLowerCase().includes(query.toLowerCase()) ||
        (airline.airline_code && airline.airline_code.toLowerCase().includes(query.toLowerCase()))
      );
      resolve(filtered);
    }, 300);
  });
};

export const getAirlineById = (id) => {
  return new Promise((resolve) => {
    setTimeout(() => {
      const airline = airlinesData.find(a => a.id === parseInt(id));
      resolve(airline);
    }, 200);
  });
};